package javacore.stepik.block2.step2_1.task1;

public class Boolen {
    public static void main(String[] args) {

        System.out.println(1 == 1);
        System.out.println(2 == 3);
        System.out.println("^ and !=");
        System.out.println(1 == 1 ^ 2 == 3);
        System.out.println((1 == 1) != (2 == 3));
        System.out.println("| and ||");
        System.out.println((1 == 1) | (2 == 3));
        System.out.println((1 == 1) || (2 == 3));
        System.out.println("& and ==");
        System.out.println((1 == 1) & (2 == 3));
        System.out.println((1 == 1) == (2 == 3));
        System.out.println("| and !=");
        System.out.println((1 == 1) | (2 == 3));
        System.out.println((1 == 1) != (2 == 3));

    }
}
